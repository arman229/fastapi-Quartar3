from fastapi import APIRouter

router = APIRouter()


@router.get("/queryparameters/{item_id}")
async def queryparameters(item_id: str, q: str | None = None, short: bool = False):
    item = {"item_id": item_id}
    if q:
        item.update({"q": q})
    if not short:
        item.update(
            {"description": "This is an amazing item that has a long description"}
        )
    return item